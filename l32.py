""" Задача 32: Определить индексы элементов массива (списка), значения которых принадлежат заданному диапазону 
(т.е. не меньше заданного минимума и не больше заданного максимума) """

# Считываем список чисел
array = input("Введите список чисел через пробел: ").split()
array = [int(num) for num in array]

# Считываем минимальное и максимальное значение
min_value = int(input("Введите минимальное значение: "))
max_value = int(input("Введите максимальное значение: "))

# Создаем пустой список для хранения индексов
indices = []

# Просматриваем каждый элемент списка и проверяем условие
for i in range(len(array)):
    if min_value <= array[i] <= max_value:
        indices.append(i)

# Выводим результат
print("Индексы элементов, соответствующих заданному диапазону:")
print(indices)
