""" Задача 30:  Заполните массив элементами арифметической прогрессии. 
Её первый элемент, разность и количество элементов нужно ввести с клавиатуры. 
Формула для получения n-го члена прогрессии: an = a1 + (n-1) * d.
Каждое число вводится с новой строки. """

# Считываем первый элемент, разность и количество элементов прогрессии
a1 = int(input("Введите первый элемент прогрессии: "))
d = int(input("Введите разность прогрессии: "))
n = int(input("Введите количество элементов прогрессии: "))

# Создаем пустой массив для хранения элементов прогрессии
progression = []

# Заполняем массив элементами арифметической прогрессии
for i in range(n):
    element = a1 + i * d
    progression.append(element)

# Выводим результат
print("Элементы арифметической прогрессии:")
for element in progression:
    print(element)
