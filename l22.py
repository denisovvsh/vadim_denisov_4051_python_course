""" Задача 22: Даны два неупорядоченных набора целых чисел (может быть, с повторениями). 
Выдать без повторений в порядке возрастания все те числа, которые встречаются в обоих наборах.
Пользователь вводит 2 числа. 
n — кол-во элементов первого множества. 
m — кол-во элементов второго множества. 
Затем пользователь вводит сами элементы множеств. """

def common_elements(n, m):
    set1 = set()
    set2 = set()

    # Ввод элементов первого множества
    print("Введите элементы первого множества:")
    for i in range(n):
        num = int(input())
        set1.add(num)

    # Ввод элементов второго множества
    print("Введите элементы второго множества:")
    for j in range(m):
        num = int(input())
        set2.add(num)

    # Находим общие элементы
    common = sorted(list(set1.intersection(set2)))

    # Вывод результатов
    print("Общие элементы, отсортированные по возрастанию:")
    for num in common:
        print(num)

# Ввод количества элементов первого и второго множества
n = int(input("Введите количество элементов первого множества: "))
m = int(input("Введите количество элементов второго множества: "))

# Вызов функции
common_elements(n, m)
